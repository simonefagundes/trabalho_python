valor_um= int(input("Insira o primeiro número: "))
valor_dois= int(input("Insira o segundo número: "))
soma = valor_um + valor_dois
subtracao = valor_um - valor_dois
multiplicacao = valor_um * valor_dois
divisao = valor_um / valor_dois

print("Resultado da soma: " +str(soma))
print("Resultado da subtração: " +str(subtracao))
print("Resultado da multiplicação: " +str(multiplicacao))
print("Resultado da divisão: " +str(divisao))